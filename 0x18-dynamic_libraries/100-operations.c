/**
 * add - function to add two integers
 * @a - first integer
 * @b - second integer
 * Return: result
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function to subtract two integers
 * @a - first integer
 * @b - second integer
 * Return: result
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function that multiplies two integers
 * @a - first integer
 * @b - second integer
 * Return - result
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function to divide two integers
 * @a - first integer
 * @b - second integer
 * Return: result
 */

int div(int a, int b)
{
	return (a / b);
}


/**
 * mod - function to find the remainder of two numbers divided
 * @a - first integer
 * @b - second integer
 * Return - remainder
 */

int mod(int a, int b)
{
	return (a % b);
}
